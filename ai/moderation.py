import logging

from openai.types.responses import Response

from ai.config import OPENAI_CHAT_MODEL, CLUB_INFO_POST_SLUGS
from ai.openai import openai
from ai.tools import search_posts
from posts.models.post import Post
from users.models.user import User

log = logging.getLogger(__name__)

MODERATION_TEMPERATURE = 0.3
MODERATION_MAX_OUTPUT_TOKENS = 800

POST_QUALITY_PROMPT = """Ты помощник модераторов нашего коммьюнити, которое называется Вастрик Клуб.
Твоя цель помогать модераторам оценивать качество новых постов, находить нарушения правил и давать оценку.
- Ориентируйся на ценности и правила Клуба при оценке качества
- Не будь всегда добрым и вежливым, ты модератор, твоя работа — следить за качеством и чистотой нашего коммьюнити
- Обращай внимание на потенциально опасные, запрещенные или горячие темы, которые могут вызвать срач
- Оцени качество текста: насколько тема уникальна и важна, насколько полно описана, нет ли типичных ИИ-запахов сгенерированного текста
- Оцени слог самого автора: насколько он агрессивен или негативно настроен, пытается ли нас обмануть, либо наоборот легкий и веселый
- Для постов оцени их длину, количество картинок, качество и дизайн текста — насколько он легко читается и хорошо проилюстрирован
- Для вопросов, тредов, батлов обрати внимание на дубликаты — мы не хотим абсолютно одинаковых вопросов
- Ежемесячные треды типа мемов или поиска работы запускаются каждый месяц заново. Обрати внимание чтобы внутри треда были ссылки на предыдущие.
- Оценив все факторы, дай посту финальную оценку от 1 (плохой контент) до 5 (отличный уникальный) и короткое заключение от себя, например "можно одобрять", "потенциальный дубликат", "слишком коротко", "потенциально написан ИИ", "только в комнату" — ты можешь придумать свои статусы тоже в зависимости от ситуации.
- В конце подсвети все обнаруженные тобой красные флаги или проблемы. Если ничего проблемного нет — ничего не пиши в этом разделе.
- Пиши коротко и ёмко. Не рассуждай, не объясняй, не хвали посты или авторов. Используй очень короткие фразы. Говори только важное.

Твой ответ обязан быть составлен по следующей форме (используй только html теги <b> и <a>):

<b>Оценка поста от ИИ: 5/5 ✅ можно одобрять</b>

<b>Статистика:</b> [N] слов, [N] разделов, [M] картинок, ...
<b>Уникальность поста:</b> 3/5
<b>Качество и дизайн текста:</b> 5/5
<b>Слог автора:</b> 4/5
<b>Вероятность срача:</b> 1/5

[если есть похожие посты] <b>Похожие посты:</b>
- [<a href="">Список ссылок</a>]

[если есть проблемы: коротко красные флаги, на которые обратить внимание модератору]
"""

def ai_rate_post_quality(post: Post) -> str:
    moderation_guides = Post.visible_objects()\
        .filter(slug__in=CLUB_INFO_POST_SLUGS)

    post_duplicates = search_posts(
        query=post.title,
        post_type=post.type,
        order_by="-created_at",
        limit=5
    )
    post_duplicates = [p for p in post_duplicates if p.get("id") != post.id]

    response: Response = openai.responses.create(
        model=OPENAI_CHAT_MODEL,
        instructions=POST_QUALITY_PROMPT,
        input=[
            {"role": "user", "content": "Основные правила и ценности Клуба:\n\n"
                                        + "\n\n".join(f"{p.title}.\n\n{p.text}" for p in moderation_guides)
            },
            {"role": "user", "content": f"Новый пост выглядит вот так:\n"
                                        f"Тип: {post.type}\n"
                                        f"Видимость наружу: {post.is_public}\n"
                                        f"Категория (комната): {post.room_id}\n"
                                        f"Название: {post.title}\n"
                                        f"Текст (Markdown): {post.text}"
            },
            {"role": "user", "content": f"Похожие посты: {post_duplicates}"},
        ],
        temperature=MODERATION_TEMPERATURE,
        max_output_tokens=MODERATION_MAX_OUTPUT_TOKENS,
        truncation="auto",
        store=False,
    )

    log.info(f"CHATGPT: {response}")
    return response.output_text



